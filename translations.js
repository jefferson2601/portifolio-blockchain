let currentLang = 'pt'; // Default language

function toggleLanguage() {
    currentLang = currentLang === 'pt' ? 'en' : 'pt';
    updateContent();
    document.getElementById('langBtn').textContent = currentLang === 'pt' ? 'EN' : 'PT';
    // Update HTML lang attribute
    document.documentElement.lang = currentLang === 'pt' ? 'pt-br' : 'en';
}

function updateContent() {
    // Update navigation menu
    document.querySelector('a[href="#home"]').textContent = translations[currentLang].inicio;
    document.querySelector('a[href="#about"]').textContent = translations[currentLang].sobre;
    document.querySelector('a[href="#teams"]').textContent = translations[currentLang].certificacoes;
    document.querySelector('a[href="#skills"]').textContent = translations[currentLang].habilidades;
    document.querySelector('a[href="#projetos"]').textContent = translations[currentLang].projetos;
    document.querySelector('a[href="#contact"]').textContent = translations[currentLang].contato;

    // Update logo
    document.querySelector('.logo a span').textContent = translations[currentLang].portfolio;

    // Update home section
    document.querySelector('.text-1').textContent = translations[currentLang].ola;
    document.querySelector('.text-3').textContent = translations[currentLang].eu_sou;
    document.querySelector('.home-content a').textContent = translations[currentLang].contratar;

    // Update about section
    document.querySelector('.about .title').textContent = translations[currentLang].sobre_titulo;
    document.querySelector('.about .right p').textContent = translations[currentLang].sobre_texto_completo;
    document.querySelector('.about .right a').textContent = translations[currentLang].mais_info;

    // Update skills section
    document.querySelector('.skills .title').textContent = translations[currentLang].habilidades;
    document.querySelector('.skills .left .text').textContent = translations[currentLang].habilidades_titulo;
    document.querySelector('.skills .left p').textContent = translations[currentLang].habilidades_texto;
    document.querySelector('.skills .left a').textContent = translations[currentLang].mais_info;

    // Update certifications section
    document.querySelector('#teams .title').textContent = translations[currentLang].certificacoes;
    
    // Update all certification cards
    const certificationCards = document.querySelectorAll('.teams .card');
    certificationCards.forEach(card => {
        const cardText = card.querySelector('.text, .text1, .text2');
        const cardDesc = card.querySelector('p');
        
        if (cardText.textContent.includes('Fundamentos')) {
            cardText.textContent = translations[currentLang].fundamentos_web3;
            cardDesc.textContent = translations[currentLang].fundamentos_desc;
        } else if (cardText.textContent.includes('Smart Contracts')) {
            cardText.textContent = translations[currentLang].smart_contracts;
            cardDesc.textContent = translations[currentLang].smart_contracts_desc;
        } else if (cardText.textContent.includes('Foundry')) {
            cardText.textContent = translations[currentLang].foundry;
            cardDesc.textContent = translations[currentLang].foundry_desc;
        } else if (cardText.textContent.includes('Hyperledger')) {
            cardText.textContent = translations[currentLang].hyperledger;
            cardDesc.textContent = translations[currentLang].hyperledger_desc;
        } else if (cardText.textContent.includes('Solidity')) {
            cardText.textContent = translations[currentLang].solidity;
            cardDesc.textContent = translations[currentLang].solidity_desc;
        } else if (cardText.textContent.includes('Vulnerabilidade')) {
            cardText.textContent = translations[currentLang].vulnerabilidade;
            cardDesc.textContent = translations[currentLang].vulnerabilidade_desc;
        } else if (cardText.textContent.includes('Engenharia')) {
            cardText.textContent = translations[currentLang].engenharia;
            cardDesc.textContent = translations[currentLang].engenharia_desc;
        }
    });

    // Update projects section
    document.querySelector('#projetos .title').textContent = translations[currentLang].projetos_titulo;
    
    // Update project cards
    const projectCards = document.querySelectorAll('#projetos .card');
    projectCards.forEach(card => {
        const cardText = card.querySelector('.text');
        const cardDesc = card.querySelector('p');
        
        if (cardText.textContent.includes('Max Blockchain')) {
            cardText.textContent = translations[currentLang].projeto_max;
            cardDesc.textContent = translations[currentLang].projeto_max_desc;
        } else if (cardText.textContent.includes('Slime Wallet')) {
            cardText.textContent = translations[currentLang].projeto_slime;
            cardDesc.textContent = translations[currentLang].projeto_slime_desc;
        } else if (cardText.textContent.includes('Auditor de Contratos')) {
            cardText.textContent = translations[currentLang].projeto_auditor;
            cardDesc.textContent = translations[currentLang].projeto_auditor_desc;
        } else if (cardText.textContent.includes('Número Secreto')) {
            cardText.textContent = translations[currentLang].projeto_numero;
            cardDesc.textContent = translations[currentLang].projeto_numero_desc;
        } else if (cardText.textContent.includes('Amigo secreto')) {
            cardText.textContent = translations[currentLang].projeto_amigo;
            cardDesc.textContent = translations[currentLang].projeto_amigo_desc;
        }
    });

    // Update contact section
    document.querySelector('.contact .title').textContent = translations[currentLang].contato;
    document.querySelector('.contact .left .text').textContent = translations[currentLang].fale_conosco;
    document.querySelector('.p-contato').textContent = translations[currentLang].contato_texto;
    
    // Update form labels and placeholders
    document.querySelector('#nome').placeholder = translations[currentLang].nome;
    document.querySelector('#telefone').placeholder = translations[currentLang].telefone;
    document.querySelector('#email').placeholder = translations[currentLang].email;
    document.querySelector('#mensagem').placeholder = translations[currentLang].escrever;
    document.querySelector('.button button').textContent = translations[currentLang].enviar;
    
    // Update footer
    document.querySelector('.criado-por p').textContent = translations[currentLang].criado_por;
} 